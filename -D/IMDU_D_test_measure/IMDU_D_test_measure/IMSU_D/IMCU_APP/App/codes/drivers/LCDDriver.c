static void change_8_bytes(unsigned char * pSource, unsigned char * pData)
{
    unsigned char i;

    for(i = 0; i < 8; i++)//点阵旋转转换
	{
        *(pData + i) = (((*(pSource + 0) >> (7 - i)) & 0x01) << 0)
	    	         + (((*(pSource + 1) >> (7 - i)) & 0x01) << 1)
	    	         + (((*(pSource + 2) >> (7 - i)) & 0x01) << 2)
	    	         + (((*(pSource + 3) >> (7 - i)) & 0x01) << 3)
	    	         + (((*(pSource + 4) >> (7 - i)) & 0x01) << 4)
	    	      	 + (((*(pSource + 5) >> (7 - i)) & 0x01) << 5)
	    	         + (((*(pSource + 6) >> (7 - i)) & 0x01) << 6)
	    	         + (((*(pSource + 7) >> (7 - i)) & 0x01) << 7);
    }
}
