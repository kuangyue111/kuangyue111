static int set_lcd_cursor(unsigned char x, unsigned char y)
{
	if (x > MAX_Y)
	{
		x = MAX_Y;
	}
	if (y > MAX_X)
	{
		y = MAX_X;
	}

  dv_LCD.CurrCursor.x = x;
  dv_LCD.CurrCursor.y = y;
  
#ifdef LCD_LINE_4	
	LCD_CMD_Data = 0x40;            //Sets the display RAM display start line address
	lcd_ready();
#endif	

	LCD_CMD_Data = 0xa0;//sets the display ram address SEG output correspondence ,bit0:0 normal,1 reverse
	lcd_ready();
	x = x / 8;
	LCD_CMD_Data = 0xb0 |(x & 0x0f);          //sets pages
	lcd_ready();
	LCD_CMD_Data = 0x10 |((y >> 4) & 0x0f);   //sets column address upper 4bits
	lcd_ready();
	LCD_CMD_Data = 0x00 |(y & 0x0f);          //sets column address lower 4bits
	lcd_ready();

  return(1);
}

