/*=============================================================================================
** @Static function declaration
**===========================================================================================*/

/*=============================================================================================
** @Interface function definition
**===========================================================================================*/
/*=============================================================================================
** @Name      : void GroupSohDisplayInit(void)
** @Input     : void
** @Output    : void
** @Function  : SOH平滑显示初始化
** @The notes : 用户初始化调用
**===========================================================================================*/
void GroupSohDisplayInit(void)
{
	gGSysCapInfo_61[eSysCapInfo61_SOH] = GetGRealSohMilliAPI();
}

/*=============================================================================================
** @Name      : void GroupSohDisplayTask(void)
** @Input     : void
** @Output    : void
** @Function  : SOH平滑显示任务
** @The notes : 100ms
**===========================================================================================*/
void GroupSohDisplayTask(void)
{
    u16 soh = 0;

    soh = GetGRealSohMilliAPI();

    gGSysCapInfo_61[eSysCapInfo61_SOH] = soh;
}

